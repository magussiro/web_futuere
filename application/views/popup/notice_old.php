            <div class="gray12">
			為降低資訊不對稱，保障投資人權益，集中市場設有價證券暫停及恢復交易機制，得分為二種型態，概述如下：<br><br>
			<b>臺灣存託憑證（TDR）暫停及恢復交易機制</b><br>
			自100年10月3日起實施，第二上市公司因有股價敏感資料待公布或發生重大事件等特殊原因，經臺灣存託憑證所表彰之有價證券上市掛牌證券交易所或證券市場公告暫停其交易者，
			證交所得公告上市臺灣存託憑證及相關認購（售）權證暫停交易。經臺灣存託憑證所表彰之有價證券上市掛牌證券交易所或證券市場公告恢復其交易，且未有因暫停交易情事違反本
			公司重大訊息相關規定，且個案情節重大，有停止臺灣存託憑證買賣必要之情事者，本公司得公告恢復其交易。<br><br>
			<b>訊息面暫停及恢復交易機制</b><br>
			自105年1月15日起實施，規範上市公司預計於營業日交易時間內(下午5時前)公開或召開董事會決議特定重大事項時，應主動向證交所申請暫停交易，並於相關訊息充分公開或說明後
			，再行申請恢復交易。另上市公司發現重大影響股東權益或證券價格之媒體報導或其他資訊，但無法於發現當日完整說明者，亦應主動向證交所申請暫停交易；倘上市公司無法於發
			現當日說明亦未申請暫停交易者，證交所於必要時得主動暫停該上市公司有價證券之交易，俟相關訊息公開並充分揭露後恢復交易。<br><br></div>
