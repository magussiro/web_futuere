            <div class="subtil_1" style="background-color:#F7F0E8;height:20px;">&nbsp;陸、鉅額交易</div>
            <div class="gray12">
              一、交易時間：委託及撮合成交時間相同<br>
              <table cellspacing="0" cellpadding="0" border="0" align="center" width="100%">
                <tbody><tr>
                  <td valign="top" width="30">(一)</td>
                  <td>配對交易<br>08:00-08:30及09：00-17：00</td>
                </tr>
                <tr>
                  <td valign="top" width="30">(二)</td>
                  <td>逐筆交易<br>09：00-17：00</td>
                </tr>
              </tbody></table>
              <br>
              二、交易單位：買賣數量之申報，得以一股(單位)或其整倍數為之。
              <br><br>
              三、申報買賣價格範圍：與一般交易完全相同。
              <br><br>
              四、升降單位：一般交易漲跌停價格範圍內，逐筆及配對交易申報買賣價格升降單位均為0.01元。
              <br><br>
              五、鉅額交易最低數額標準<br>
              <table cellspacing="0" cellpadding="0" border="0" align="center" width="100%">
                <tbody><tr>
                  <td valign="top" width="30">(一)</td>
                  <td>單一證券：數量達500交易單位以上或金額達1,500萬元以上。</td>
                </tr>
                <tr>
                  <td valign="top" width="30">(二)</td>
                  <td>股票組合：5種股票以上且總金額達1,500萬元以上。</td>
                </tr>
              </tbody></table>
              <br>
              六、委託有效性：盤前配對交易時段未成交之委託，於當日其他交易時段內仍屬有效。
              <br><br>
              七、交割期別：採普通（T+2日）交割。
              <br><br>
              八、預收款券<br>
              除另有規定外（如全額交割股、處置證券及權證等），證券商接受鉅額買賣委託時，得視情形自行決定向投資人預收足額或一定成數之款券。
              <br><br>
              九、競價方式<br>
              <table cellspacing="0" cellpadding="0" border="0" align="center" width="100%">
                <tbody><tr>
                  <td valign="top" width="30">(一)</td>
                  <td>
                    逐筆交易：採逐筆競價，隨到隨撮。
                    <br>1. 單一證券撮合優先順序及成交價格，依逐筆輸入之買進申報或賣出申報別，依下列原則決定之：
                    <br>　(1) 當筆輸入之買進申報價格高於或等於先前輸入之最低賣出申報價格時，依賣出申報價格由低至高依
                    <br>　　　序成交，如申報之賣價有數筆相同時，按申報時間優先順序依序成交，直至完全滿足或當筆輸入之
                    <br>　　　買進申報價格低於未成交之賣出申報價格為止。
                    <br>　(2) 當筆輸入之賣出申報價格低於或等於先前輸入之最高買進申報價格時，依買進申報價格由高至低依
                    <br>　　　序成交，如申報之買價有數筆相同時，按申報時間優先順序依序成交，直至完全滿足或當筆輸入之
                    <br>　　　賣出申報價格高於未成交之買進申報價格為止。
                    <br>2. 股票組合之撮合成交，當筆輸入之買進（賣出）申報各股票代號、單價及數量，應與先前輸入之賣出（買進）申報均相符，再與賣出（買進）申報時間最優先者成交。
                  </td>
                </tr>
                <tr>
                  <td valign="top" width="30">(二)</td>
                  <td>配對交易：依證券商輸入之交易對象、數量及價格撮合成交。</td>
                </tr>
              </tbody></table>
