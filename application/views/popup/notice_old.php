                    <br>　(1) 外資買進申報時，該數量即時累入外資額度控管。
                    <br>　(2) 外資賣出申報成交後，自外資控管額度釋放該數量。
                    <br>2. 配對交易：買賣證券商完成當組配對交易申報後，屬外資之買賣採淨額計算，即時納入外資額度控管。
                  </td>
                </tr>
              </tbody></table>
