              <br><br>
              二、交易單位：由拍賣委託人訂定之交易單位及其倍數為拍賣單位。
              <br><br>
              三、每日升降幅度：<br>
              <table cellspacing="0" cellpadding="0" border="0" align="center" width="100%">
                <tbody><tr>
                  <td valign="top" width="30" rowspan="4">(一)</td>
                  <td colspan="2">拍賣底價：除公股釋出外，以拍賣當日開盤競價基準上下百分之十五幅度範圍內為限。但有價證券無升降幅度限制者，得以拍賣當日收盤價格上下百分之十五幅度範圍內為限。以拍賣當日收盤價格為計算拍賣底價之基準者，若遇該證券當日無收盤價格，則拍賣底價依下列原則決定之價格為計算基準，以其上下百分之十五幅度範圍內為限：</td>
                </tr>
                <tr>
                  <td valign="top">1.&nbsp;</td>
                  <td>當日收盤時最高買進申報價格高於當日開盤競價基準，則為該最高買進申報價格。</td>
                </tr>
                <tr>
                  <td valign="top">2.&nbsp;</td>
                  <td>當日收盤時最低賣出申報價格低於當日開盤競價基準，則為該最低賣出申報價格。</td>
                </tr>
                <tr>
                  <td valign="top">3.&nbsp;</td>
